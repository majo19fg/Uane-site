                  <p style="text-align: justify;">Como cada año la familia UANE llevó a cabo sus juegos InterUANE, en esta ocasión el campus Monclova abrió sus puertas para celebrar una vez más estas competencias deportivas.</p>

                  <p style="text-align: justify;">La inauguración se llevó a cabo el día 13 de marzo en la Unidad Deportiva “Nora Leticia Rocha”, ubicada en la cuidad de Monclova, Coahuila.</p>
                   
                  <p style="text-align: justify;"> Esa ocasión la Universidad reunió a más de 970 alumnos de los campus Saltillo, Torreón, Sabinas Piedras Negras, Matamoros, Reynosa, Monterrey y Monclova, quienes se enfrentaron a competencias deportivas de las disciplinas de básquetbol, fútbol, tochito, voleibol, sóftbol y porra mixta. Así como más de 150 funcionarios que engalanaron el evento con su presencia.</p>
                    

                  <p style="text-align: justify;">Para dar aún más realce a la ocasión, la Mtra. Carolina Tovar, directora del campus anfitrión, dio unas palabras de bienvenida invitando a todos los participantes a disfrutar de las competencias y a intercambiar experiencias con sus compañeros.</p>

                  <p style="text-align: justify;">Se contó con la presencia de la escolta y banda de guerra del 105° Batallón de Infantería, así como el Licenciado Jorge Eduardo Verástegui, rector de UANE, la Licenciada Lucía Azucena Ramos, Secretaria de Turismo. Posteriormente recibimos la visita del Ing. Alfredo Paredes López, Alcalde de la ciudad de Monclova.</p>

                  <p style="text-align: justify;">Para dar por inaugurada la tan esperada jornada, el alumno David Elizandro Gonzalez dirigió la toma de protesta a todos los atletas asistentes.</p>

                  <p style="text-align: justify;">Durante la noche del 14 de marzo, se llevó a cabo la tradicional competencia de porra mixta donde los campus de Saltillo, Torreón, Monclova, Sabinas, Piedras Negras y Monterrey dieron todo su esfuerzo compitiendo, contamos con la presencia de Horacio Matuk, Coach de Porristas del Club Búfalos, certificado en Gimnasio y experiencia de 6 años en dicha disciplina, y de Carlos Ramírez; Head Coach, director general de la Academia Regio Cheer All Stars de Monterrey, Nuevo León y con 14 años de experiencia en dicha disciplina participando jurado calificador quien otorgó la tradicional estatuilla en forma de oso, así como medallas a cada uno de los alumnos competidores. En esta ocasión en primer lugar quedó el campus Torreón; en segundo lugar, campus Saltillo y, en tercer lugar, campus Sabinas.</p>

                  <p style="text-align: justify;">Tras tres días que los equipos dejaron todo en las canchas, se dio el cierre el día 15 de marzo con grandes resultados donde la armonía, solidaridad y hermandad se hicieron presentes, dejándonos deseOSOS de la siguiente edición, de la cual próximamente se revelará la sede.</p>
