                  <p style="text-align: justify;">En esta ocasión hablaremos de otro valor que es pilar dentro del ámbito académico, me refiero a la responsabilidad.</p>

                  <p style="text-align: justify;">La responsabilidad se considera una cualidad y un valor del ser humano. Se trata de una característica positiva de las personas que son capaces de comprometerse y actuar de forma correcta dentro de un cargo o rol que le ha tocado vivir.</p>

                  <p style="text-align: justify;">Encontré un escrito muy interesante del Dr. Alberto Cormillot que nos habla de algunas claves para lograr que una persona mejore su responsabilidad; creo que adaptan perfectamente al nivel académico y se pueden poner en práctica:</p>

                  <p style="text-align: justify;">1. <b>No asignes culpas: </b>No pierdas el tiempo culpando a los demás de tus acciones, por el contrario, ocúpate de lo que te toca hacer y …… hazlo.</p>

                  <p style="text-align: justify;">2. <b>Arriésgate: </b>Todos los seres humanos aprenden con la teoría del ensayo-error. Si no pruebas, seguro no sabrás si funcionan. Así que ten el coraje y arriésgate, aprende de cada acción que realices.</p>

                  <p style="text-align: justify;">3. <b>No malinterpretes: </b>Evita hacer juicios o comentarios sobre los errores de alguien, así como dar por hecho acciones antes de comprobarlas.</p>
                  
                  <p style="text-align: justify;">4. <b>Evita el éxito pasajero: </b>El alcanzar una meta no significa que dejes de luchar, al contrario, lucha siempre por cosas nuevas. Exígete para que todo salga como lo tienes planeado.</p>

                  <p style="text-align: justify;">5. <b>Actitud positiva: </b>Levántate todos los días, deja a un lado la negatividad y la flojera, enfócate en cumplir tus objetivos. Con esto serás más responsable y te ganarás la confianza de los demás.</p>

                  <p style="text-align: justify;">6. <b>Toma tus propias decisiones: </b>Por más difícil que parezca, esta medida es clave no dejes que los demás elijan por ti, tú eres el dueño de tu vida, de tu imagen y de tus pensamientos. </p>

                  <p style="text-align: justify;"><b>Invito a toda la comunidad educativa a vivir este pilar de la universidad, el valor de la responsabilidad.</b></p>
