              <h2 class="text-uppercase font-weight-600 mt-0 font-20 text-theme-colored"  align='center'>AVISO DE PRIVACIDAD PARA LA PROTECCIÓN DE DATOS PERSONAL</h2>
                <h2 class="text-uppercase font-weight-600 mt-0 font-20 text-theme-colored" align='center'>
                 DE LA UNIVERSIDAD AUTÓNOMA DEL NORESTE A.C</h2>
                </br>
               <p align="justify">De conformidad a lo previsto en la Ley Federal de Protección de Datos Personales en
                Posesión de los Particulares (para fines prácticos denominada a lo largo del presente aviso
                como la Ley), la Universidad Autónoma del Noreste A.C. (en lo sucesivo UANE) en su
                carácter de institución de educación media y superior establece y hace de su
                conocimiento el presente Aviso de Privacidad:</p>
               </br>
                <h4 class="text-uppercase font-weight-600 mt-0 font-14 line-bottom text-theme-colored">Términos y Condiciones</h4>
                <p align="justify"><strong>1.</strong> El presente Aviso de Privacidad tiene por objeto la protección de los datos personales
                    de los miembros de nuestra comunidad universitaria mediante su tratamiento legítimo,
                    controlado e informado, a efecto de garantizar su privacidad, así como el derecho del
                    lector de este Aviso a la autodeterminación informativa.</p>
                <p align="justify" ><strong>2.</strong> Se entiende por <strong>Dato Personal</strong> cualquier información concerniente a una persona
                física identificada o identificable.</p>
                <p align="justify"><strong>3.</strong> El domicilio de UANE para los efectos relacionados con el presente Aviso y la
                    consolidación de los datos que se recaben en sus diversos Campus y Centros de Servicio
                    Universitario, es en la calle Monclova número 1561 de la colonia República en la Ciudad
                    de Saltillo, Coahuila.</p>
                <p align="justify" ><strong>4.</strong> Al proporcionar sus Datos Personales por escrito, a través de una solicitud, vía
                    telefónica, formato digital, correo electrónico, o cualquier otro documento, el lector del
                    presente Aviso <strong> acepta y autoriza a la Universidad Autónoma del Noreste A.C. a
                    utilizar y tratar de forma automatizada los datos personales e información
                    suministrados</strong>, los cuales formarán parte de nuestra base de datos para ser destinados a
                    los siguientes fines: identificación, ubicación, comunicación, contacto continuo con la
                    comunidad universitaria, envío de información y/o bienes, la prestación de los servicios
                    educativos y demás inherentes a UANE, integración de expedientes académico y
                    administrativo, análisis solicitudes de ingreso y de beca en su caso, descuentos o
                    recargos, evaluación de la calidad en el servicio, la implementación de estadísticas sobre
                    los servicios educativos y actividades deportivas y culturales y demás inherentes a UANE,
                    compartir los datos requeridos por nuestra aseguradora para el acceso a servicios
                    médicos de emergencia de acuerdo a las políticas de UANE, realizar pruebas
                    toxicológicas, así como para enviarlos y/o transferirlos a terceros, dentro y fuera del
                    territorio nacional, por cualquier medio que permita la ley para cumplir con nuestros
                    fines sociales.</p>
                <p align="justify" ><strong>5. </strong>Pueden ser objeto de tratamiento los siguientes datos personales, datos personales
                    sensibles y datos personales financieros o patrimoniales, que se indican a continuación:</p>
                    <p align="justify" >  a. <strong>Datos de identificación: </strong>Nombre completo, edad, fecha y lugar de
                        nacimiento, género, estado civil, domicilio, nacionalidad, correo electrónico,
                        teléfonos de contacto, Clave Única del Registro de Población, Número de
                        Seguridad Social, clave del Registro Federal de Contribuyentes, firma,
                        religión, huella digital, nombres de los Padres, hermanos y cónyuge en su
                        caso.</p>
                    <p align="justify" >  b. <strong>Datos académicos: </strong>Nombre y domicilio de escuela de procedencia, trayectoria
                        educativa, calificaciones y certificados emitidos por otras instituciones,
                        idiomas, título profesional, número de cédula profesional, certificados de
                        estudios, antecedentes escolares.</p>
                    <p align="justify" >  c. <strong>Datos de salud: </strong>Estado de salud, historial clínico, enfermedades,
                        restricciones alimenticias, alergias, grupo sanguíneo, donador de órganos,
                        cuestiones de carácter psicológico y psiquiátrico, datos de su doctor, 
                        tratamientos médicos o psicológicos que recibe, datos de contactos para caso
                        de emergencia.</p>
                    <p align="justify" >  d. <strong>Datos sobre los padres o tutores: </strong>Nombre completo del padre, la madre o
                        tutor del alumno, nombre completo, edad y escolaridad de todos sus hijos,
                        domicilio completo de la familia o de cada uno de los padres si están
                        separados, así como sus teléfonos y correos electrónicos, datos completos del
                        domicilio del trabajo, empleo o negocio del padre y/o de la madre incluyendo
                        teléfonos y correos, ocupación, puesto que desempeña, grado de estudios,
                        estado civil, horarios del trabajo, Registro Federal de Contribuyentes, Clave
                        Única de Registro de Población y domicilio fiscal para la facturación
                        electrónica.</p>     
                    <p align="justify" >  e. <strong>Datos financieros o patrimoniales: </strong>Bienes muebles e inmuebles, ingresos y
                        egresos personales, ingresos y egresos de los padres, cuentas bancarias,
                        referencias personales, información fiscal, reporte de buro de crédito,
                        declaraciones de impuestos, recibos de nómina y en general datos sobre la
                        situación económica de la familia.</p> 
                    <p align="justify" >  f. <strong>Datos sensibles: </strong>Aficiones, deportes, creencias religiosas, afiliación política,
                        afiliación sindical, asociaciones religiosas o políticas a las que ha pertenecido,
                        actividades extracurriculares, datos sobre el comportamiento del alumno,
                        candidatos a empleados y empleados, en lo social, familiar, emotivo, salud,
                        deportivo; datos solicitados en entrevistas de admisión y/o de seguimiento
                        que permitan evaluar al alumno, candidatos a empleados y empleados y su
                        familia o entorno familiar.</p>
                    <p align="justify" >  g. <strong>Características físicas: </strong>Foto, color de piel, de iris y de cabello, huella digital,
                        tatuajes, cicatrices o señas particulares</p>                                                                                                
                    <p align="justify" >En caso de no contar con algunos de los datos citados, podría existir la posibilidad de que
                        el Responsable no pueda proporcionar los servicios propios de la relación jurídica que se
                        establezca con el Titular.</p>  
                        <p align="justify" >Asimismo, se informa que para cumplir con las finalidades previstas en el presente Aviso,
                        serán recabados y tratados datos personales sensibles descritos en los incisos anteriores.
                        Es por esto que el Responsable se compromete a que los mismos sean tratados bajo
                        estrictas medidas de seguridad, garantizando su confidencialidad.</p>  

            <p align="justify" ><strong>6.</strong> Mediante la aceptación y autorización para el tratamiento de tus datos personales en
                los términos antes señalados, faculta expresamente a UANE a transferirlos a autoridades
                de cualquier nivel (Federales, Estatales, Municipales), organismos públicos y privados,
                diversas empresas y/o personas físicas, dentro y fuera de México, con el propósito de
                certificar sus estudios y competencias, así como para participar en sus procesos de
                selección de personal y aplicar a los diversos puestos de trabajo vacantes que éstas
                publiquen en nuestra Bolsa de Trabajo; a aseguradoras para todo lo relacionado con el
                seguro colectivo de accidentes personales y seguros de gastos médicos en general que
                llegasen a contratarse, a aquellas instituciones nacionales o extranjeras con las cuales la
                UANE tiene celebrado convenio de intercambio académico para la realización de servicio
                social, prácticas profesionales, diversas actividades escolares, deportivas, culturales y
                académicas.</p> 
                <p align="justify" ><strong>7.</strong> La temporalidad del manejo de los Datos Personales que se proporcionen será
                    indefinida a partir de la fecha en que los mismos sean puestos a nuestra disposición,
                    pudiendo oponerse usted al manejo de los mismos en cualquier momento que lo
                    considere oportuno, con las limitaciones de Ley; en caso de que su solicitud de oposición
                    sea procedente, la UANE dejará de manejar estos Datos Personales sin ninguna
                    responsabilidad de nuestra parte. Quedan fuera de este supuesto las Bases de Datos
                    referentes a las calificaciones y demás información académica de los alumnos, ex 
                    alumnos y egresados de la UANE y alumnos en movilidad o en intercambio provenientes
                    de otras instituciones.</p> 
            <p align="justify" ><strong>8.</strong>  El área de la Universidad Autónoma del Noreste A.C. responsable del tratamiento de
                datos personales, está obligada a cumplir con los principios de licitud, consentimiento,
                información, calidad, finalidad, lealtad, proporcionalidad y responsabilidad tutelados en
                la Ley; por tal motivo con fundamento en los artículos 13 y 14 de la Ley Federal de
                Protección de Datos Personales en Posesión de los Particulares. Asimismo el área de
                Relaciones Laborales así como encargados de Bolsa de Trabajo se comprometen a guardar
                estricta confidencialidad de los datos personales, así como a mantener las medidas de
                seguridad administrativas, técnicas y físicas que permitan protegerlos contra cualquier
                daño, pérdida, alteración, acceso o tratamiento no autorizado.</p>      
            <p align="justify" ><strong>9.</strong> En términos de lo establecido por el artículo 22 de la citada Ley, tiene usted derecho en
                cualquier momento al acceso, rectificación, cancelación y oposición (derechos ARCO) al
                tratamiento de sus datos personales mediante la solicitud vía correo electrónico dirigido al
                Licenciado Antonio Abdalah Villegas Díaz aavillegas@uane.edu.mx; estableciendo como
                asunto del correo "Derechos ARCO”. </p> 
            <p align="justify" >El Responsable le comunicará, en un plazo máximo de veinte días hábiles, contados
                desde la fecha en que aquél reciba la solicitud correspondiente, la determinación
                adoptada. Si la solicitud resulta procedente, ésta se hará efectiva dentro de los quinces
                días hábiles siguientes a la fecha en que el Responsable comunique la respuesta. En caso
                de que la información proporcionada en su solicitud resulta errónea o insuficiente, o no
                se acompañen los documentos necesarios para acreditar su identidad o la representación
                legal correspondiente, el Responsable, dentro de los cinco días hábiles siguientes a la
                recepción de su solicitud, requerirá la subsanación de las deficiencias para poder dar
                trámite a la misma. En estos casos, usted contará con diez días hábiles para atender el
                requerimiento de subsanación, contados a partir del día siguiente en que lo hubiere
                recibido. La solicitud correspondiente se tendrá por no presentada si usted no responde
                dentro de dicho plazo. Usted podrá obtener la información o datos personales solicitados
                a través de copias simples, documentos electrónicos en formatos convencionales (Word,
                PDF, etc.), mediante acceso restringido y autorizado al sistema que trata sus datos
                personales o a través de cualquier otro medio legítimo que garantice y acredite el ejercicio
                efectivo del derecho solicitado.
                </p> 
            <p align="justify" ><strong>10.</strong> Usted podrá revocar su consentimiento para el tratamiento de sus datos personales,
                sin efectos retroactivos, en todos aquellos casos en que dicha revocación no suponga la
                imposibilidad de cumplir obligaciones derivadas de una relación jurídica vigente entre
                usted y el Responsable y/o UANE.</p> 
            <p align="justify" >El procedimiento para la revocación del consentimiento, en su caso, será el mismo que el
                establecido en el apartado inmediato anterior para el ejercicio de los derechos ARCO.</p> 
            <p align="justify" ><strong>11.</strong> Usted podrá limitar el uso o divulgación de sus datos personales dirigiendo la
                solicitud correspondiente mediante el procedimiento establecido para el ejercicio de los
                derechos ARCO.</p>
            <p align="justify" ><strong>12.</strong> La UANE ocasionalmente modificará y corregirá este Aviso de Privacidad, por lo tanto
                le pedimos que revise este aviso regularmente en la página de internet:
                www.uane.edu.mx.</p> 
            <p align="justify" ><strong>13.</strong> La UANE protegerá sus Datos Personales en los términos de la Ley, y le comunicará
                los elementos contenidos en las fracciones del artículo 16 de la Ley.</p> 
            <p align="justify" ><strong>14.</strong> El hecho de cubrir el pago de inscripción a la Universidad Autónoma del Noreste A.C.
                en cualquiera de sus Campus y/o modalidades de educación y seleccionar asignaturas, 
                es un hecho que presume tu total aceptación al contenido del presente Aviso de
                Privacidad.</p>  
